        static void Main(string[] args)
        {
            string savePath = Directory.GetCurrentDirectory();
            int families = 5;
            int generations = 5;
            if (args.Length > 0)
            {
                savePath = args[0];
                if(args.Length > 1)
                {
                    families = System.Convert.ToInt32(args[1]);
                }
                if(args.Length > 2)
                {
                    generations = System.Convert.ToInt32(args[2]);
                }
            }

            List<Race> allRaces = new List<Race>()
            {
                Race_Orc, Race_HalfOrc, Race_Halfling, Race_Gnome, Race_Human, Race_Centaur, Race_Minotaur, Race_WoodElf, Race_HighElf, Race_Drow, Race_Tortle, Race_Goliath, Race_Dwarf, Race_Dragonborn, Race_Lizardfolk, Race_Shifter, Race_Tabaxi, Race_Tiefling, Race_Goblin
            };


            for (int i = 0; i < allRaces.Count; i++)
            {
                string[] names = new string[families];
                for (int j = 0; j < names.Length; j++)
                {
                    names[j] = "Race " + allRaces[i].Name + (j + 1);
                }
                FamilyGenerator.Generate(savePath, generations, names, allRaces[i], new FantasyDate(Age.Generic_Age, 5000), "WorldRaces", firstHasSiblings: false);
            }

        }
